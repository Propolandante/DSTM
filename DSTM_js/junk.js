	function create_snake()
	{
		var length = 5;
		snake_array = [];
		for (var i = length-1; i >= 0; i--)
		{
			snake_array.push({x:i+2,y:2});
		}
	}
	
	function create_food()
	{
		food = {
			x: Math.round(Math.random()*((cvs_width - cell_width)/cell_width)),
			y: Math.round(Math.random()*((cvs_height - cell_width)/cell_width))
		};
		
		if (map[food.x][food.y]) // If food is out of bounds, try again
		{
			create_food();
		}
	}
	
	function paint() // this also seems to be an update function. If I had more time I would split this into 2 more specific functions
	{
		// to avoid trailing objects (snake), repaint canvas every frame (can be optimized)
		context.fillStyle = "white";
		context.fillRect(0,0,cvs_width,cvs_height);
		context.strokeStyle = "black";
		context.strokeRect(0,0,cvs_width,cvs_height);
		
		// to move snake, move tail to head
		
		var nx = snake_array[0].x; // current head x position
		var ny = snake_array[0].y; // current head y position
		
		// move head in direction
		// THis section has been fixed to ACTUALLY prevent player from reversing/committing suicide
		// In the tutorial, this check was in the input-handling section, which is independent of the game_loop interval
		// If buttons were pressed fast enough, player was able to kill itself within a single game frame, which is no good
		if(newdir == "right")
		{
			if(dir != "left")
			{
				dir = newdir;
				nx++;
			}
		}
		else if(newdir == "left")
		{
			if(dir != "right")
			{
				dir = newdir;
				nx--;
			}
		}
		else if(newdir == "up")
		{
			if(dir != "down")
			{
				dir = newdir;
				ny--;
			}
		}
		else if(newdir == "down")
		{
			if(dir != "up")
			{
				dir = newdir;
				ny++;
			}
		}
		
		//check for player death
		
		if(check_collision(nx, ny, snake_array) || map[nx][ny])
		{
			//restart game
			init(difficulty);
			
			return;
		}
		
		//if player reaches food
		if(nx == food.x && ny == food.y)
		{
			var tail = {x: nx, y: ny}; // create new snake segment in front of head (will become head)
			score+= difficulty;
			
			if(score > high_score)
			{
				high_score = score;
			}
			
			create_food();
		}
		
		else
		{
			var tail = snake_array.pop(); // pops out last cell
			
			// replace tail in front of head (will become head)
			tail.x = nx;
			tail.y = ny;
		}
		
		snake_array.unshift(tail); // puts tail at the head of the array
		
		// paint the map
		
		for(var i = 0; i < map.length; i++)
		{
			for(var j = 0; j < map[i].length; j++)
			{
				var mc = map[i][j];
				
				if(mc == 1)
				{
					paint_cell(i,j,"wall");
				}
			}
		}
		
		// paint the new snake
		
		for(var i = 0; i < snake_array.length; i++)
		{
			var c = snake_array[i];
			paint_cell(c.x,c.y, "snake");
		}
		
		// paint the food
		
		paint_cell(food.x, food.y, "food");
		
		// paint the score
		context.font = "15px Georgia";
		context.fillStyle = "red";
		var score_text = "Score: " + score;
		context.fillText(score_text, 45, cvs_height-35);
		
		var high_score_text = "High Score: " + high_score;
		context.fillText(high_score_text, 115, cvs_height-35);
		
		// paint the extra text
		
		context.fillStyle = "blue";
		var escape_text = "Press ESC for Main Menu";
		context.fillText(escape_text, 230, cvs_height-35);
		
	}
	
	// generic paint function
	function paint_cell(x,y,type)
	{
		if(type == "snake")
		{
			context.fillStyle = "blue";
			context.fillRect(x*cell_width, y*cell_width, cell_width, cell_width);
			context.strokeStyle = "white";
			context.strokeRect(x*cell_width, y*cell_width, cell_width, cell_width);
		}
		
		if(type == "food")
		{
			context.fillStyle = "yellow";
			context.fillRect(x*cell_width, y*cell_width, cell_width, cell_width);
			context.strokeStyle = "white";
			context.strokeRect(x*cell_width, y*cell_width, cell_width, cell_width);
		}
		
		if(type == "wall"){
			context.fillStyle = "gray";
			context.fillRect(x*cell_width, y*cell_width, cell_width, cell_width);
			context.strokeStyle = "black";
			context.strokeRect(x*cell_width, y*cell_width, cell_width, cell_width);
		}
		
	}
	
